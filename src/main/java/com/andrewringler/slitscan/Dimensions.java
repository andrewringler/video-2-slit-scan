package com.andrewringler.slitscan;

public class Dimensions {
	public final int width;
	public final int height;
	public Dimensions(int width, int height) {
		this.width = width;
		this.height = height;
	}
	
	public int getWidth() {
		return width;
	}
	
	public int getHeight() {
		return height;
	}
}
